package day6;

public class MathTest {

	public static void main(String[] args) {
		System.out.println(Math.E);
		System.out.println(Math.PI);
		System.out.println(Math.random());
		System.out.println(Math.abs(-10));
		System.out.println(Math.sqrt(9));
		System.out.println(Math.ceil(3.567));
		System.out.println(Math.floor(3.567));
		System.out.println(Math.round(3.567));
		System.out.println(Math.ceil(3.267));
		System.out.println(Math.floor(3.267));
		System.out.println(Math.round(3.267));
		System.out.println(Math.max(10, 20));
		System.out.println(Math.min(10, 20));
	}
}
